const mongoose = require("mongoose");
const UserSchema = mongoose.Schema

// to create new schema
const UserSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now()
  }
});

// export model user with UserSchema
module.exports = User = mongoose.model("user", UserSchema);

